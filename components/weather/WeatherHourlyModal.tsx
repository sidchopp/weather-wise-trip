"use client";

import { Fragment, ReactElement, useState } from "react";
import { Dialog, Transition } from "@headlessui/react";
import { WeatherHourlyChart } from "./WeatherHourlyChart";
import { ChartBarIcon } from "@/assets/icons";
import { WeatherData } from "@/types/weather";

interface WeatherHourlyModalProps {
  weatherData: WeatherData;
}

const WeatherHourlyModal = ({
  weatherData,
}: WeatherHourlyModalProps): ReactElement => {
  let [isOpen, setIsOpen] = useState(false);

  function closeModal() {
    setIsOpen(false);
  }

  function openModal() {
    setIsOpen(true);
  }

  return (
    <>
      <div className="inset-0 flex py-2 items-center justify-end">
        <button
          type="button"
          onClick={openModal}
          className="p-1 inline-flex items-center justify-center text-base font-medium text-center dark:text-gray-200 bg-blue-500 rounded-lg hover:bg-blue-600 hover:scale-105"
        >
          <ChartBarIcon />
        </button>
      </div>

      <Transition appear show={isOpen} as={Fragment}>
        <Dialog as="div" className="relative z-10" onClose={closeModal}>
          <Transition.Child
            as={Fragment}
            enter="ease-out duration-300"
            enterFrom="opacity-0"
            enterTo="opacity-100"
            leave="ease-in duration-200"
            leaveFrom="opacity-100"
            leaveTo="opacity-0"
          >
            <div className="fixed inset-0 bg-black opacity-90" />
          </Transition.Child>

          <div className="fixed inset-0 overflow-y-auto">
            <div className="flex min-h-full items-center justify-center p-4 text-center">
              <Transition.Child
                as={Fragment}
                enter="ease-out duration-300"
                enterFrom="opacity-0 scale-95"
                enterTo="opacity-100 scale-100"
                leave="ease-in duration-200"
                leaveFrom="opacity-100 scale-100"
                leaveTo="opacity-0 scale-95"
              >
                <Dialog.Panel className="w-full max-w-5xl transform overflow-hidden rounded-2xl bg-white p-6 text-left align-middle shadow-xl transition-all">
                  <Dialog.Title
                    as="h3"
                    className="text-lg font-medium leading-6 text-gray-900"
                  >
                    Next 2 days Forecast
                  </Dialog.Title>
                  <WeatherHourlyChart weatherData={weatherData} />

                  <div className="mt-4">
                    <button
                      type="button"
                      className="inline-flex justify-center rounded-md border border-transparent bg-blue-100 px-4 py-2 text-sm font-medium text-blue-900 hover:bg-blue-200 focus:outline-none focus-visible:ring-2 focus-visible:ring-blue-500 focus-visible:ring-offset-2"
                      onClick={closeModal}
                    >
                      Back
                    </button>
                  </div>
                </Dialog.Panel>
              </Transition.Child>
            </div>
          </div>
        </Dialog>
      </Transition>
    </>
  );
};

export { WeatherHourlyModal };
